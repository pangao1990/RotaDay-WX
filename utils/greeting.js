function positiveModulo(value, divisor) {
  return ((value % divisor) + divisor) % divisor;
}

function compose(date, openings, endings) {
  const dayNumber = Math.floor(Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()) / DAY_MS);
  return `${openings[positiveModulo(dayNumber, openings.length)]}，${endings[positiveModulo(dayNumber, endings.length)]}`;
}

function getDailyGreeting(date = new Date()) {
  if (date.getHours() < 10) return compose(date, MORNING_OPENINGS, MORNING_ENDINGS);
  if (date.getHours() < 18) return compose(date, DAY_OPENINGS, DAY_ENDINGS);
  return compose(date, EVENING_OPENINGS, EVENING_ENDINGS);
}

